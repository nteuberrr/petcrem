-- Banco de imágenes para campañas de mailing (generadas con Nano Banana Pro o
-- subidas a mano). Viven en R2 (url/key) y se reciclan entre correos: el
-- generador IA revisa este banco y reutiliza una imagen cuando calza.
create table if not exists "mailing_imagenes" (
  "id" bigint generated by default as identity primary key,
  "url" text not null default '',
  "key" text not null default '',
  -- codigo: id legible y estable para referirse a la imagen (chat del agente + banco).
  -- i-N = foto suelta/subida; C-X.Y = pieza de campaña (portada/placa/carrusel).
  "codigo" text not null default '',
  "descripcion" text not null default '',
  "prompt" text not null default '',
  "tags" text not null default '',
  "alt" text not null default '',
  "grupo" text not null default '',
  -- subgrupo: etiqueta libre opcional (ej. por campaña) para ordenar sin crear grupos nuevos.
  "subgrupo" text not null default '',
  -- whatsapp: 'TRUE' si el agente de WhatsApp puede enviar esta imagen al cliente
  -- cuando la pida (ej. fotos de ánforas/urnas). Lo marca el equipo a mano.
  "whatsapp" text not null default 'FALSE',
  -- favorita: 'TRUE' si el equipo la destacó con la estrella en el banco.
  "favorita" text not null default 'FALSE',
  "aspect" text not null default '',
  "ancho" text not null default '',
  "alto" text not null default '',
  "origen" text not null default '',
  "modelo" text not null default '',
  "creado_por" text not null default '',
  "fecha_creacion" text not null default ''
);
alter table "mailing_imagenes" enable row level security;
-- Idempotente para tablas ya creadas antes de estas columnas.
alter table "mailing_imagenes" add column if not exists "whatsapp" text not null default 'FALSE';
alter table "mailing_imagenes" add column if not exists "subgrupo" text not null default '';
alter table "mailing_imagenes" add column if not exists "codigo" text not null default '';
alter table "mailing_imagenes" add column if not exists "favorita" text not null default 'FALSE';

